// ============================================================================
// 第一节：平衡二叉搜索树
// ============================================================================

/**
 * AVL树定义：
 *
 * AVL树是一种自平衡二叉搜索树。
 * - 对任意节点，左右子树的高度差不超过1。
 * - 平衡因子 = 左子树高度 - 右子树高度
 * - 平衡不变量：所有节点满足 |平衡因子| ≤ 1
 * - 当插入或删除破坏了这个不变量，通过旋转重新平衡。
 *
 * 性质：
 * - 树高保证为 O(log n)
 * - 最大高度 ≈ 1.44 log2(n+2) - 0.328
 * - 比红黑树更严格平衡
 * - 读密集型工作负载查找更快
 * - 插入删除平均需要比红黑树更多旋转
 */

/**
 * AVL树 vs 红黑树对比：
 *
 * 1. 平衡不变量：
 *    - AVL：对所有节点 |左高度 - 右高度| ≤ 1 → **严格平衡**
 *    - 红黑：颜色规则保证最长路径 ≤ 2 × 最短路径 → **宽松（近似）平衡**
 *
 * 2. 高度边界：
 *    - AVL：高度 ≤ ~1.44 log2(n)
 *    - 红黑：高度 ≤ 2 log2(n)
 *
 * 3. 性能：
 *    - AVL：查找更快（平衡更好），插入删除更慢（需要更多旋转）
 *    - 红黑：查找稍慢，插入删除更快（实际旋转更少）
 *
 * 4. 为什么C++ STL的map/set使用红黑树：
 *    - 大多数实际工作负载都是插入、删除和查找混合
 *    - 红黑树整体旋转更少，平均性能更好
 *    - 对于动态变化数据结构，常数因子更低
 *    - O(log n)保证已经足够，AVL的更紧平衡大多数场景不需要
 *    - 红黑树删除操作更容易正确实现
 *
 * 5. 使用场景：
 *    - AVL：适合以读为主、查找性能关键的只读数据
 *    - 红黑：适合通用平衡BST，频繁修改的场景
 *
 * 总结：
 * AVL严格平衡 → 查找更好，插入删除更差。
 * 红黑近似平衡 → 查找稍差，插入删除更好。
 * STL选择红黑是因为大多数应用数据修改频繁，整体性能更好。
 */

// ============================================================================
// 第二节：B树 vs B+树
// ============================================================================

/**
 * B树：
 *
 * B树是为块存储设备（磁盘）设计的平衡搜索树。
 * - 所有节点同时存储键和数据/值
 * - 内部节点存储键用于索引，叶子节点也存储数据
 * - 保持有序便于高效查找
 * - 度数m约束：除根外，所有节点有⌈m/2⌉ 到 m 个孩子
 * - 所有叶子在同一深度
 */

/**
 * B+树：
 *
 * B+树是B树的变种，为顺序访问和范围查询优化。
 * - **所有数据都存在叶子节点**；内部节点只存储索引键（不存数据）
 * - 叶子节点用链表按顺序链接起来
 * - 内部节点因为只存键，所以体积更小
 * - 保持和B树相同的平衡性质
 */

/**
 * B树和B+树的主要区别：
 *
 * 1. **数据存储**：
 *    - B树：内部节点和叶子节点都同时存储键和数据
 *    - B+树：内部节点只存键，所有数据都存在叶子节点
 *
 * 2. **查找行为**：
 *    - B树：在内部节点找到就可以停止
 *    - B+树：总是要查到叶子节点 → 性能更稳定一致
 *
 * 3. **范围查询**：
 *    - B树：范围查询需要在树中四处跳转遍历
 *    - B+树：叶子已经有序链接 → 找到起点后直接遍历链表 → **范围扫描效率高得多**
 *
 * 4. **内部节点大小**：
 *    - B+树：内部节点只存键（不存数据）→ 一个磁盘块能放更多键
 *      → 查找需要更少IO操作 → 查找更快
 *
 * 5. **删除操作**：
 *    - B树：从内部节点删除更复杂，因为数据可能在内部
 *    - B+树：所有删除都在叶子进行（父节点仍保留键做索引）→ 更简单
 *
 * 6. **缓存效率**：
 *    - B+树：内部节点更小 → 更多节点能放进内存缓存 → 减少磁盘访问
 */

/**
 * 为什么数据库索引用B+树：
 *
 * 1. **磁盘IO效率更高**：
 *    内部节点更小，一个磁盘块能放更多键 →
 *    找到一个键需要更少磁盘读 → 延迟更低。
 *
 * 2. **范围查询高效**：
 *    叶子节点已经链接在一起，扫描整个范围只需要遍历叶子链表，
 *    不需要回树上爬。这在数据库查询（WHERE id BETWEEN x AND y）非常常见。
 *
 * 3. **性能更可预测**：
 *    所有查找都要查到叶子层，性能均匀一致。
 *    B树有些查找可以在内部节点提前结束，但性能波动更大。
 *
 * 4. **插入删除更简单**：
 *    所有修改都在叶子层进行，结构更规则更容易维护。
 *
 * 5. **缓存利用率更好**：
 *    因为内部节点更小，更多能缓存到内存中，减少磁盘访问次数。
 *
 * 总结：
 * 数据库数据存在磁盘上，磁盘IO是瓶颈。
 * B+树最小化磁盘IO次数，并且让范围查询极其高效，
 * 这正是数据库索引需要的。
 */

// ============================================================================
// 第三节：红黑树五条性质
// ============================================================================

/**
 * 红黑树的五条性质：
 *
 * 1. 每个节点要么是红色，要么是黑色。
 * 2. 根节点是黑色。
 * 3. 所有叶子节点（NIL哨兵）都是黑色。
 * 4. 如果一个节点是红色，那么它的两个孩子都是黑色。
 *    （任何路径上不会有两个连续红色节点）
 * 5. 从任意节点到后代叶子节点的所有路径，包含相同数量的黑色节点。
 *
 * 结论：根到叶子的最长路径最多是最短路径的两倍 → 树近似平衡。
 * 高度保证：高度 ≤ 2 * log2(n + 1)
 */

// ============================================================================
// 第四节：二叉搜索树（BST）性质
// ============================================================================

/**
 * 二叉搜索树不变量：
 *
 * 对任意键值为k的节点：
 * - 左子树所有键值都小于k
 * - 右子树所有键值都大于k
 *
 * BST的中序遍历得到有序序列。
 *
 * 时间复杂度：
 * - 平均：查找/插入/删除都是 O(log n)
 * - 最坏情况（不平衡，退化成链表）：O(n)
 */

