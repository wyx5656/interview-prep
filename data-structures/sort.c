#include <stdio.h>
#include <stdlib.h>

/*
 * 八大排序算法实现 - 详细注释版
 *
 * 排序算法分类：
 *   1. 比较排序：通过元素间的比较来确定排序
 *      - 交换排序：冒泡排序、快速排序
 *      - 插入排序：插入排序、希尔排序
 *      - 选择排序：选择排序、堆排序
 *      - 归并排序：归并排序
 *   2. 非比较排序：基数排序（基于分配和收集）
 */

// 辅助函数：交换两个元素的值
void swap(int *a, int *b) {
    int temp = *a;
    *a = *b;
    *b = temp;
}

// 辅助函数：打印数组
void printArray(int arr[], int n) {
    for (int i = 0; i < n; i++) {
        printf("%d ", arr[i]);
    }
    printf("\n");
}

/* ============================================================================
 * 1. 冒泡排序 (Bubble Sort)
 *
 * 【原理】
 *   重复地走访过要排序的数列，一次比较两个相邻元素，如果它们的顺序错误就把它们交换过来。
 *   越大的元素会经过交换慢慢"浮"到数列的顶端，所以叫冒泡排序。
 *
 * 【实现步骤】
 *   1. 比较相邻的两个元素，如果第一个比第二个大，就交换它们
 *   2. 对每一对相邻元素做同样的工作，从开始第一对到结尾的最后一对
 *   3. 这步做完后，最后的元素会是最大的数，下一趟就不用考虑它了
 *   4. 重复以上步骤，直到没有任何一对数字需要比较
 *
 * 【优化】
 *   如果某一趟中没有发生任何交换，说明数组已经有序，可以提前退出循环。
 *
 * 【时间复杂度】
 *   - 最优：O(n)，当数组已经有序时，只需要一趟扫描
 *   - 最坏：O(n²)，当数组逆序时
 *   - 平均：O(n²)
 *
 * 【空间复杂度】O(1)，原地排序
 * 【稳定性】稳定排序（相等元素不会交换位置）
 * ============================================================================
 */
void bubbleSort(int arr[], int n) {
    // 外层循环：需要n-1趟，每一趟确定一个最大元素放到末尾
    for (int i = 0; i < n - 1; i++) {
        int swapped = 0; // 优化标记：记录这一趟是否发生了交换

        // 内层循环：比较相邻元素，把最大的冒泡到后面
        // 已经确定的i个最大元素在末尾，不需要再比较，所以到n-i-1
        for (int j = 0; j < n - i - 1; j++) {
            // 如果前一个比后一个大，交换它们
            if (arr[j] > arr[j + 1]) {
                swap(&arr[j], &arr[j + 1]);
                swapped = 1; // 标记发生了交换
            }
        }

        // 如果这一趟没有交换，说明已经有序，直接退出
        if (!swapped) break;
    }
}

/* ============================================================================
 * 2. 选择排序 (Selection Sort)
 *
 * 【原理】
 *   每次从待排序的数组中选出最小（或最大）的一个元素，放到起始位置，
 *   然后再从剩余的未排序元素中继续找，直到全部排完。
 *
 * 【实现步骤】
 *   1. 在未排序序列中找到最小元素，存放到排序序列的起始位置
 *   2. 再从剩余未排序元素中继续寻找最小元素
 *   3. 重复第二步，直到所有元素均排序完毕
 *
 * 【特点】
 *   交换移动数据次数少，因为每次只交换一次。当交换代价大时选择排序更优。
 *
 * 【时间复杂度】不管有序无序，都是O(n²)
 * 【空间复杂度】O(1)，原地排序
 * 【稳定性】不稳定排序，例如：[5, 5, 2]，第一次选2和第一个5交换，两个5的相对顺序改变
 * ============================================================================
 */
void selectionSort(int arr[], int n) {
    // 外层循环：i是已排序区域的末尾，i从0到n-2，最后一个自然有序
    for (int i = 0; i < n - 1; i++) {
        // 在未排序区域[i+1, n-1]中找到最小值的索引
        int minIdx = i; // 先假设i位置就是最小的
        for (int j = i + 1; j < n; j++) {
            if (arr[j] < arr[minIdx]) {
                minIdx = j; // 更新最小值索引
            }
        }

        // 如果找到更小的，交换到i位置
        if (minIdx != i) {
            swap(&arr[i], &arr[minIdx]);
        }
        // 现在arr[0..i]已经有序
    }
}

/* ============================================================================
 * 3. 插入排序 (Insertion Sort)
 *
 * 【原理】
 *   类似于打扑克牌摸牌，把当前牌插入到前面已经排好序的合适位置。
 *   将数组分为已排序区和未排序区，每次从未排序区取出第一个元素，
 *   在已排序区中从后往前扫描，找到合适位置插入。
 *
 * 【实现步骤】
 *   1. 第一个元素默认是已排序的，从第二个元素开始往前扫描
 *   2. 取出当前元素key，在已排序区中从后往前找
 *   3. 如果当前已排序元素比key大，就把它向后移动一位
 *   4. 继续向前找，直到找到一个元素小于等于key，把key插入到它后面
 *
 * 【特点】
 *   对于基本有序的数组效率很高，因为移动次数少。
 *   数据量越小，插入排序越快，所以很多高级排序会用插入排序优化小数组。
 *
 * 【时间复杂度】
 *   - 最优：O(n)，数组已经有序，每次只比较一次
 *   - 最坏：O(n²)，数组逆序
 *   - 平均：O(n²)
 * 【空间复杂度】O(1)，原地排序
 * 【稳定性】稳定排序
 * ============================================================================
 */
void insertionSort(int arr[], int n) {
    // i从1开始，因为第一个元素默认已经有序
    for (int i = 1; i < n; i++) {
        int key = arr[i]; // 当前要插入的元素
        int j = i - 1;    // j从已排序区的最后开始往前找

        // 从后往前遍历已排序区：如果元素比key大，就向后移动
        // 这样给key腾出插入位置
        while (j >= 0 && arr[j] > key) {
            arr[j + 1] = arr[j]; // 向后移动
            j--;                 // 继续向前找
        }

        // 此时j位置的元素 <= key，所以key插入到j+1位置
        arr[j + 1] = key;
    }
}

/* ============================================================================
 * 4. 希尔排序 (Shell Sort) - 缩小增量排序
 *
 * 【原理】
 *   希尔排序是插入排序的改进版，核心思想是分组插入排序。
 *   先将整个数组按照某个增量gap分成若干个子序列，分别对每个子序列做插入排序，
 *   然后逐步缩小增量，继续分组排序，直到增量为1，此时整个数组基本有序，
 *   最后做一次整体插入排序即可完成。
 *
 * 【为什么比直接插入排序快？】
 *   当gap大时，每组元素少，排序快；gap小时，数组已经基本有序，插入排序快。
 *
 * 【增量选择】
 *   最初Shell建议gap = n/2，然后gap /= 2，直到gap=1，这也是本实现采用的方法。
 *   更好的增量序列有Knuth序列、Sedgewick序列等。
 *
 * 【时间复杂度】取决于增量序列，大约O(n^1.3) ~ O(n^2)
 * 【空间复杂度】O(1)，原地排序
 * 【稳定性】不稳定排序，因为分组交换可能改变相等元素的相对顺序
 * ============================================================================
 */
void shellSort(int arr[], int n) {
    // gap是增量，每次减半，直到gap=0停止
    for (int gap = n / 2; gap > 0; gap /= 2) {

        // 对每个分组做插入排序
        // i从gap开始，相当于每个分组的第二个元素，开始插入排序
        for (int i = gap; i < n; i++) {
            int temp = arr[i];     // 当前要插入的元素
            int j;

            // 在同一个分组中向前找，如果前一个元素更大，就后移
            // j - gap就是同组中前一个元素的位置
            for (j = i; j >= gap && arr[j - gap] > temp; j -= gap) {
                arr[j] = arr[j - gap]; // 后移
            }

            // 插入到正确位置
            arr[j] = temp;
        }
    }
}

/* ============================================================================
 * 5. 快速排序 (Quick Sort)
 *
 * 【原理】
 *   分治思想：选一个基准元素pivot，把数组分成两部分，
 *   左边部分都 <= pivot，右边部分都 >= pivot，然后递归地排序左右两部分。
 *
 * 【分区(partition)实现思路】
 *   这里采用 Lomuto 分区方案：
 *   1. 选最后一个元素作为基准pivot
 *   2. 用i记录小于等于pivot区域的边界，初始在low-1
 *   3. j从low遍历到high-1，如果arr[j] <= pivot，i向右扩展，交换arr[i]和arr[j]
 *   4. 遍历结束后，把pivot交换到i+1位置，此时pivot左边都小，右边都大
 *   5. 返回pivot的位置i+1
 *
 * 【优化】
 *   - 三数取中法选基准：避免有序数组时每次选端点导致O(n²)
 *   - 尾递归优化：减少递归深度
 *   - 小数组切换到插入排序
 *
 * 【时间复杂度】
 *   - 平均：O(nlogn)，每次分区都比较均匀
 *   - 最坏：O(n²)，每次分区都只分出一个元素（有序数组选端点时）
 * 【空间复杂度】O(logn) ~ O(n)，递归栈深度
 * 【稳定性】不稳定排序
 * ============================================================================
 */

// 分区函数：将arr[low..high]按照pivot分区，返回pivot的最终位置
int partition(int arr[], int low, int high) {
    int pivot = arr[high];      // 选择最后一个元素作为基准pivot
    int i = low - 1;            // i是小于等于pivot区域的右边界，初始在low左边

    // 遍历low到high-1，逐个和pivot比较
    for (int j = low; j < high; j++) {
        if (arr[j] <= pivot) {
            i++;                // 扩展小于等于区域
            swap(&arr[i], &arr[j]); // 当前元素放到小于等于区域
        }
    }

    // 最后把pivot放到分界点i+1，这样pivot左边都<=它，右边都>=它
    swap(&arr[i + 1], &arr[high]);
    return i + 1; // 返回pivot的位置
}

// 递归辅助函数：排序arr[low..high]
void quickSortHelper(int arr[], int low, int high) {
    if (low < high) {
        // pi是分区后基准的位置，已经在正确位置
        int pi = partition(arr, low, high);

        // 递归排序pi左边和右边
        quickSortHelper(arr, low, pi - 1);
        quickSortHelper(arr, pi + 1, high);
    }
}

// 快速排序入口
void quickSort(int arr[], int n) {
    quickSortHelper(arr, 0, n - 1);
}

/* ============================================================================
 * 6. 归并排序 (Merge Sort)
 *
 * 【原理】
 *   分治思想的典型应用：
 *   1. 分解：把待排序数组分成两个子数组，从中间分开
 *   2. 解决：递归地排序两个子数组
 *   3. 合并：把两个已经有序的子数组合并成一个有序数组
 *
 * 【合并过程】
 *   创建一个临时数组，用双指针i,j分别指向两个有序数组的头部，
 *   比较arr[i]和arr[j]，把较小的放到结果数组中，指针后移，
 *   直到一个指针走完，把另一个数组剩余部分复制过去。
 *
 * 【时间复杂度】不管有序无序，都是O(nlogn)，因为每次分解都是二分，合并是O(n)
 * 【空间复杂度】O(n)，需要额外空间存储临时数组
 * 【稳定性】稳定排序，合并的时候相等元素先放左边的，相对顺序不变
 * ============================================================================
 */

// 合并函数：将已经有序的arr[left..mid]和arr[mid+1..right]合并成一个有序数组
void merge(int arr[], int left, int mid, int right) {
    int i, j, k;
    int n1 = mid - left + 1;  // 左子数组长度
    int n2 = right - mid;     // 右子数组长度

    // 分配临时空间存储两个子数组
    int *L = (int *)malloc(n1 * sizeof(int));
    int *R = (int *)malloc(n2 * sizeof(int));

    // 把原数组数据复制到临时数组
    for (i = 0; i < n1; i++)
        L[i] = arr[left + i];
    for (j = 0; j < n2; j++)
        R[j] = arr[mid + 1 + j];

    // 合并两个有序数组回原数组的left..right位置
    i = 0;  // L的当前指针
    j = 0;  // R的当前指针
    k = left; // 原数组当前位置
    while (i < n1 && j < n2) {
        if (L[i] <= R[j]) {
            arr[k] = L[i]; // 取较小的放进去
            i++;
        } else {
            arr[k] = R[j];
            j++;
        }
        k++;
    }

    // 把L或R中剩余的元素复制过去
    while (i < n1) {
        arr[k] = L[i];
        i++;
        k++;
    }
    while (j < n2) {
        arr[k] = R[j];
        j++;
        k++;
    }

    // 释放临时空间
    free(L);
    free(R);
}

// 递归辅助函数：排序arr[left..right]
void mergeSortHelper(int arr[], int left, int right) {
    if (left < right) {
        // 计算中点，避免(left+right)溢出
        int mid = left + (right - left) / 2;

        // 递归排序左右两部分
        mergeSortHelper(arr, left, mid);
        mergeSortHelper(arr, mid + 1, right);

        // 合并两个有序部分
        merge(arr, left, mid, right);
    }
}

// 归并排序入口
void mergeSort(int arr[], int n) {
    mergeSortHelper(arr, 0, n - 1);
}

/* ============================================================================
 * 7. 堆排序 (Heap Sort)
 *
 * 【原理】
 *   利用堆这种数据结构的性质来排序。堆是一个完全二叉树，满足：
 *   大根堆：父节点的值 >= 左右子节点的值
 *   堆顶元素就是整个堆中的最大值。
 *
 * 【实现步骤】
 *   1. 建堆：把原始数组构建成一个大根堆（从最后一个非叶子节点开始向下调整）
 *   2. 排序：
 *      - 把堆顶（最大值）和当前堆的最后一个元素交换，这样最大值就放到了数组末尾
 *      - 堆的大小减1，对新的堆顶重新调整为大根堆
 *      - 重复这个过程，直到堆大小为1
 *
 * 【堆的数组存储】
 *   对于下标i（从0开始）：
 *   - 左子节点：2*i + 1
 *   - 右子节点：2*i + 2
 *   - 父节点：(i-1)/2
 *
 * 【时间复杂度】O(nlogn)，建堆O(n)，每次调整O(logn)，共n-1次
 * 【空间复杂度】O(1)，原地排序，只需要交换
 * 【稳定性】不稳定排序
 * ============================================================================
 */

/**
 * 堆化函数：以i为根的子树调整为大根堆
 * n是堆的当前大小，因为排序过程中堆在不断缩小
 */
void heapify(int arr[], int n, int i) {
    int largest = i;         // 先假设i就是最大的
    int left = 2 * i + 1;    // 左子节点下标
    int right = 2 * i + 2;   // 右子节点下标

    // 如果左子节点存在，且比当前最大大，更新最大
    if (left < n && arr[left] > arr[largest])
        largest = left;

    // 如果右子节点存在，且比当前最大大，更新最大
    if (right < n && arr[right] > arr[largest])
        largest = right;

    // 如果最大不是i，说明需要交换，交换后继续递归调整
    if (largest != i) {
        swap(&arr[i], &arr[largest]);
        heapify(arr, n, largest); // 递归调整被交换的子树
    }
}

// 堆排序入口
void heapSort(int arr[], int n) {
    // 第一步：建堆 - 从最后一个非叶子节点开始，从下往上调整
    // 最后一个非叶子节点下标：n/2 - 1
    for (int i = n / 2 - 1; i >= 0; i--)
        heapify(arr, n, i);

    // 第二步：一个个取出元素，排序
    for (int i = n - 1; i > 0; i--) {
        swap(&arr[0], &arr[i]); // 将当前堆顶（最大值）放到数组末尾i位置
        heapify(arr, i, 0);      // 对剩余堆重新堆化，堆大小是i
    }
}

/* ============================================================================
 * 8. 基数排序 (Radix Sort)
 *
 * 【原理】
 *   属于"分配式排序"，不基于元素比较，按照低位先排序，然后收集，
 *   再按照高位排序，然后再收集，依次类推，直到最高位。
 *   基数排序是基于计数排序实现的。
 *
 * 【步骤】（以十进制整数为例）
 *   1. 先找出最大数，得到位数d
 *   2. 从最低位（个位）到最高位，依次对每一位做计数排序
 *   3. 因为是从低到高处理，低位优先级高，高位不会打乱低位的顺序
 *   4. 处理完最高位，数组就有序了
 *
 * 【稳定性】稳定排序，基数排序要求每一趟都必须稳定，才能保证最终正确。
 *
 * 【时间复杂度】O(d * (n + k))，d是最大数的位数，k是基数（这里k=10）
 * 【空间复杂度】O(n + k)
 * 【适用场景】整数排序，尤其是当数据范围很大但位数不多时
 *
 * 注意：本实现只处理非负整数，如果有负数，可以加上偏移量处理。
 * ============================================================================
 */

// 获取数组中的最大值，用于确定最大位数
int getMax(int arr[], int n) {
    int max = arr[0];
    for (int i = 1; i < n; i++)
        if (arr[i] > max)
            max = arr[i];
    return max;
}

/**
 * 针对当前位exp（exp = 10^d）做计数排序
 * exp=1 处理个位，exp=10处理十位，以此类推
 */
void countingSort(int arr[], int n, int exp) {
    int *output = (int *)malloc(n * sizeof(int)); // 输出数组
    int count[10] = {0};                          // 每个数字(0-9)的计数

    // 第一步：统计当前位上每个数字(0-9)出现多少次
    for (int i = 0; i < n; i++)
        count[(arr[i] / exp) % 10]++;

    // 第二步：计算累积计数，count[i]表示当前位 <= i的元素有多少个
    // 这样才能知道每个元素应该放到输出数组的哪个位置
    for (int i = 1; i < 10; i++)
        count[i] += count[i - 1];

    // 第三步：从后往前遍历原数组，放到输出数组中
    // 从后往前遍历是为了保证稳定性，相等元素保持原来的相对顺序
    for (int i = n - 1; i >= 0; i--) {
        int digit = (arr[i] / exp) % 10;
        output[count[digit] - 1] = arr[i];
        count[digit]--;
    }

    // 第四步：把排序结果复制回原数组
    for (int i = 0; i < n; i++)
        arr[i] = output[i];

    free(output);
}

// 基数排序入口
void radixSort(int arr[], int n) {
    if (n <= 1) return;

    // 找到最大值，确定需要处理多少位
    int m = getMax(arr, n);

    // exp从1开始，依次是1, 10, 100, 1000...处理每一位
    for (int exp = 1; m / exp > 0; exp *= 10)
        countingSort(arr, n, exp);
}

